"""Artifact category definitions — 20 categories x ~50 variants = ~1000 test cases."""

from __future__ import annotations

from dataclasses import dataclass


@dataclass
class Category:
    name: str
    fmt: str
    ext: str
    filename: str
    sections: list[str]
    prompt_base: str
    variants: list[str]

    @property
    def count(self) -> int:
        return len(self.variants)


def _v(prefix: str, items: list[str]) -> list[str]:
    return [f"{prefix} for {item}" for item in items]


